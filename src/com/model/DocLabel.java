package com.model;

public class DocLabel 
{
    private int dlID;
    
    private int docID;
    
    private String content;

	public int getDlID() {
		return dlID;
	}

	public void setDlID(int dlID) {
		this.dlID = dlID;
	}

	public int getDocID() {
		return docID;
	}

	public void setDocID(int docID) {
		this.docID = docID;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
}
